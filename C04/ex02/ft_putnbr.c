#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	ch_nb;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	else if (nb < 0)
	{
		nb *= -1;
		write(1, "-", 1);
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
	}
	ch_nb = nb % 10 + '0';
	write(1, &ch_nb, 1);
}
